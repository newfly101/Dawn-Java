package variable;

public class Var4 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double d = 10.5; // 실수
        boolean tf = true; // 불리언(true / false)
        char c = 'A'; // 문자 하나
        String s = "Hello World"; // 문자열

        System.out.println(a);
        System.out.println(d);
        System.out.println(tf);
        System.out.println(c);
        System.out.println(s);
    }
}
